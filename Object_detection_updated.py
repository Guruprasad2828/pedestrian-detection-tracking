# Step 2: Train One Epoch
# ============================================

def train_one_epoch(model, loader,
                    optimizer, device, epoch):
    """
    Runs one full training epoch

    Returns:
        dict with average losses
    """
    model.train()

    # Loss accumulators
    total_loss      = 0.0
    loss_cls_total  = 0.0
    loss_box_total  = 0.0
    loss_rpn_total  = 0.0
    loss_obj_total  = 0.0

    n_batches       = len(loader)
    skipped         = 0

    for batch_idx, (frames, targets) in enumerate(loader):

        # Move frames to device
        frames  = [f.to(device) for f in frames]

        # Validate + move targets
        targets = validate_targets(targets, device)

        # ---- Forward Pass ----
        try:
            loss_dict = model(frames, targets)
        except Exception as e:
            skipped += 1
            if skipped <= 3:
                print(f"    Batch {batch_idx} "
                      f"skipped: {str(e)[:60]}")
            continue

        # Sum all losses
        losses = sum(
            loss for loss in loss_dict.values()
        )

        # ---- Backward Pass ----
        optimizer.zero_grad()
        losses.backward()

        # Gradient clipping
        torch.nn.utils.clip_grad_norm_(
            model.parameters(),
            max_norm = 5.0
        )

        optimizer.step()

        # ---- Accumulate Losses ----
        total_loss     += losses.item()
        loss_cls_total += loss_dict.get(
            'loss_classifier',
            torch.tensor(0.0)
        ).item()
        loss_box_total += loss_dict.get(
            'loss_box_reg',
            torch.tensor(0.0)
        ).item()
        loss_rpn_total += loss_dict.get(
            'loss_rpn_box_reg',
            torch.tensor(0.0)
        ).item()
        loss_obj_total += loss_dict.get(
            'loss_objectness',
            torch.tensor(0.0)
        ).item()

        # ---- Progress Print ----
        if (batch_idx + 1) % 50 == 0:
            avg = total_loss / (batch_idx + 1)
            lr  = optimizer.param_groups[0]['lr']
            print(f"    Batch [{batch_idx+1:4d}/{n_batches}]"
                  f"  Loss: {avg:.4f}"
                  f"  LR: {lr:.6f}")

    # ---- Compute Averages ----
    n = max(n_batches - skipped, 1)

    return {
        'total' : total_loss     / n,
        'cls'   : loss_cls_total / n,
        'box'   : loss_box_total / n,
        'rpn'   : loss_rpn_total / n,
        'obj'   : loss_obj_total / n,
    }


