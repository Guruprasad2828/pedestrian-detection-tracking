def process_mot17_sequence(
        model, seq_path,
        output_path,
        conf_threshold = 0.5,
        max_frames     = 150,
        max_age        = 5,
        min_hits       = 3,
        iou_threshold  = 0.3):
    """Full detection + tracking pipeline"""

    # Get sorted frames
    frames = sorted([
        f for f in os.listdir(seq_path)
        if f.endswith('.jpg')
        or f.endswith('.png')
    ])

    print(f"Found {len(frames)} frames")

    # Limit frames
    if max_frames:
        frames = frames[:max_frames]
    print(f"   Processing: {len(frames)}")

    # Sequence info
    seq_info = read_seq_info(seq_path)

    # Video writer
    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    out    = cv2.VideoWriter(
        output_path,
        fourcc,
        seq_info['fps'],
        (seq_info['width'], seq_info['height'])
    )

    # SORT tracker
    tracker = SORTTracker(
        max_age       = max_age,
        min_hits      = min_hits,
        iou_threshold = iou_threshold
    )

    model.eval()

    fps_history   = []
    track_history = []
    total_tracks  = set()

    print(f"\n{'='*55}")
    print(f"Processing Sequence...")
    print(f"{'='*55}")

    for idx, fname in enumerate(frames):

        t_start = time.time()

        # Read frame
        frame = cv2.imread(
            os.path.join(seq_path, fname)
        )
        if frame is None:
            continue

        orig_h, orig_w = frame.shape[:2]

        # Preprocess
        resized = cv2.resize(
            frame, (IMG_WIDTH, IMG_HEIGHT)
        )
        rgb    = cv2.cvtColor(
            resized, cv2.COLOR_BGR2RGB
        )
        tensor = torch.from_numpy(
            rgb.copy()
        ).float() / 255.0
        tensor = tensor.permute(2, 0, 1)

        # Detect
        with torch.no_grad():
            output = model(
                [tensor.to(DEVICE)]
            )[0]

        # Filter confidence
        keep   = (
            output['scores'] >= conf_threshold
        )
        boxes  = output['boxes'][keep]\
            .cpu().numpy()
        scores = output['scores'][keep]\
            .cpu().numpy()

        # Format detections
        detections = np.column_stack(
            [boxes, scores]
        ) if len(boxes) > 0 \
          else np.empty((0,5))

        # Update tracker
        tracks = tracker.update(detections)

        # Scale to original resolution
        sx = orig_w / IMG_WIDTH
        sy = orig_h / IMG_HEIGHT

        scaled = []
        for t in tracks:
            x1,y1,x2,y2,tid = t
            scaled.append([
                x1*sx, y1*sy,
                x2*sx, y2*sy,
                tid
            ])
        scaled = np.array(scaled) \
            if scaled \
            else np.empty((0,5))

        # FPS
        fps = 1.0 / max(
            time.time() - t_start, 1e-6
        )
        fps_history.append(fps)

        # Draw
        frame_out = draw_tracks(
            frame, scaled,
            fps=fps, n_dets=len(boxes),
            show_fps=True, show_count=True
        )

        out.write(frame_out)

        # Stats
        for t in tracks:
            total_tracks.add(int(t[4]))
        track_history.append(len(tracks))

        # Progress
        if (idx+1) % 30 == 0:
            avg_fps = np.mean(
                fps_history[-30:]
            )
            pct = (idx+1)/len(frames)*100
            print(
                f"  Frame {idx+1:4d}/"
                f"{len(frames)} "
                f"({pct:5.1f}%) | "
                f"FPS: {avg_fps:5.1f} | "
                f"Tracks: {len(tracks):3d}"
            )

    out.release()

    avg_fps    = np.mean(fps_history) \
        if fps_history else 0
    avg_tracks = np.mean(track_history) \
        if track_history else 0

    print(f"\n{'='*55}")
    print(f"Processing Complete!")
    print(f"{'='*55}")
    print(f"  Frames     : {len(frames)}")
    print(f"  Track IDs  : {len(total_tracks)}")
    print(f"  Avg FPS    : {avg_fps:.1f}")
    print(f"  Avg Tracks : {avg_tracks:.1f}")
    print(f"  Output     : {output_path}")

    return {
        'fps_history'  : fps_history,
        'track_history': track_history,
        'total_tracks' : total_tracks,
        'output_path'  : output_path
    }


