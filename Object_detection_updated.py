# Step 2: IoU Calculator
# ============================================

def compute_iou(box_a, box_b):
    """
    Computes IoU between two boxes
    box format: [x1, y1, x2, y2]
    """
    # Intersection
    x1 = max(box_a[0], box_b[0])
    y1 = max(box_a[1], box_b[1])
    x2 = min(box_a[2], box_b[2])
    y2 = min(box_a[3], box_b[3])

    inter = max(0, x2 - x1) * max(0, y2 - y1)

    # Union
    area_a = (
        (box_a[2] - box_a[0]) *
        (box_a[3] - box_a[1])
    )
    area_b = (
        (box_b[2] - box_b[0]) *
        (box_b[3] - box_b[1])
    )
    union  = area_a + area_b - inter

    if union <= 0:
        return 0.0

    return inter / union


# ============================================
# Step 3: Compute Precision & Recall
# ============================================

def compute_precision_recall(
        all_preds, all_targets,
        iou_threshold=0.5):
    """
    Computes Precision, Recall, AP

    Args:
        all_preds   : list of dicts
                      {'boxes', 'scores', 'labels'}
        all_targets : list of dicts
                      {'boxes', 'labels'}
        iou_threshold: float (default 0.5)

    Returns:
        dict with precision, recall, AP, F1
    """
    tp_list    = []
    fp_list    = []
    scores_list= []
    n_gt       = 0

    for preds, targets in zip(
            all_preds, all_targets):

        pred_boxes  = preds['boxes']
        pred_scores = preds['scores']
        gt_boxes    = targets['boxes']

        n_gt       += len(gt_boxes)
        matched     = [False] * len(gt_boxes)

        # Sort by confidence score
        order = np.argsort(
            -pred_scores.numpy()
        )

        for idx in order:
            pb    = pred_boxes[idx].numpy()
            score = pred_scores[idx].item()
            scores_list.append(score)

            best_iou = 0.0
            best_j   = -1

            for j, gb in enumerate(gt_boxes):
                iou = compute_iou(
                    pb, gb.numpy()
                )
                if iou > best_iou:
                    best_iou = iou
                    best_j   = j

            if (best_iou >= iou_threshold
                    and best_j >= 0
                    and not matched[best_j]):
                tp_list.append(1)
                fp_list.append(0)
                matched[best_j] = True
            else:
                tp_list.append(0)
                fp_list.append(1)

    if len(tp_list) == 0:
        return {
            'precision' : 0.0,
            'recall'    : 0.0,
            'ap'        : 0.0,
            'f1'        : 0.0
        }

    # Sort by score
    order      = np.argsort(
        -np.array(scores_list)
    )
    tp_arr     = np.array(tp_list)[order]
    fp_arr     = np.array(fp_list)[order]

    # Cumulative
    tp_cum     = np.cumsum(tp_arr)
    fp_cum     = np.cumsum(fp_arr)

    recalls    = tp_cum / max(n_gt, 1)
    precisions = tp_cum / (tp_cum + fp_cum + 1e-6)

    # AP using trapezoidal rule
    ap = np.trapz(precisions, recalls)

    # Final P, R at threshold 0.5
    precision  = precisions[-1] if len(precisions) > 0 else 0
    recall     = recalls[-1]    if len(recalls)    > 0 else 0
    f1         = (
        2 * precision * recall /
        max(precision + recall, 1e-6)
    )

    return {
        'precision'  : float(precision),
        'recall'     : float(recall),
        'ap'         : float(ap),
        'f1'         : float(f1),
        'precisions' : precisions,
        'recalls'    : recalls
    }


