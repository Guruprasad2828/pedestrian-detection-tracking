# Step 1: Target Validator
# Cleans targets before passing to model
# ============================================

def validate_targets(targets, device):
    """
    Ensures all targets are valid:
    - Removes invalid boxes
    - Ensures labels match boxes
    - Handles empty frames safely
    """
    clean = []

    for t in targets:
        boxes  = t['boxes'].to(device)
        labels = t['labels'].to(device)

        # Remove invalid boxes
        if len(boxes) > 0:
            valid  = (
                (boxes[:, 0] >= 0)           &
                (boxes[:, 1] >= 0)           &
                (boxes[:, 2] > boxes[:, 0] + 1) &
                (boxes[:, 3] > boxes[:, 1] + 1) &
                (boxes[:, 2] <= IMG_WIDTH)   &
                (boxes[:, 3] <= IMG_HEIGHT)
            )
            boxes  = boxes[valid]
            labels = labels[valid]

        # If no valid boxes → add dummy box
        # (Faster R-CNN needs at least 1 box)
        if len(boxes) == 0:
            boxes  = torch.tensor(
                [[0.0, 0.0, 10.0, 10.0]],
                dtype = torch.float32
            ).to(device)
            labels = torch.tensor(
                [1],
                dtype = torch.int64
            ).to(device)

        clean.append({
            'boxes'  : boxes,
            'labels' : labels
        })

    return clean


