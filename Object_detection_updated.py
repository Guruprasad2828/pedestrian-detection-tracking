def run_sort_on_sequence(
        model, dataset, device,
        start_idx     = 0,
        n_frames      = 50,
        conf_threshold= 0.5,
        max_age       = 5,
        min_hits      = 3,
        iou_threshold = 0.3):
    """
    Runs SORT tracker on a sequence of frames

    Returns:
        frame_results: list of track results
                       per frame
    """
    model.eval()

    tracker      = SORTTracker(
        max_age       = max_age,
        min_hits      = min_hits,
        iou_threshold = iou_threshold
    )

    frame_results = []

    # Disable augmentation
    original          = dataset.is_train
    dataset.is_train  = False

    end_idx = min(
        start_idx + n_frames,
        len(dataset)
    )

    print(f"⏳ Running SORT on frames "
          f"{start_idx} → {end_idx}...")

    for idx in range(start_idx, end_idx):

        frame, target = dataset[idx]

        # ---- Run Detector ----
        with torch.no_grad():
            output = model(
                [frame.to(device)]
            )[0]

        # ---- Filter by Confidence ----
        keep = output['scores'] >= conf_threshold

        boxes  = output['boxes'][keep].cpu().numpy()
        scores = output['scores'][keep].cpu().numpy()

        # ---- Format for SORT ----
        if len(boxes) > 0:
            detections = np.column_stack(
                [boxes, scores]
            )
        else:
            detections = np.empty((0, 5))

        # ---- Update Tracker ----
        tracks = tracker.update(detections)

        frame_results.append({
            'frame'      : idx,
            'image'      : frame,
            'tracks'     : tracks,
            'detections' : detections,
            'gt_boxes'   : target['boxes'].numpy()
        })

        if (idx - start_idx + 1) % 10 == 0:
            n_tracks = len(tracks)
            n_dets   = len(detections)
            print(f"  Frame {idx:4d} | "
                  f"Detections: {n_dets:3d} | "
                  f"Tracks: {n_tracks:3d}")

    dataset.is_train = original

    print(f"SORT complete!")
    print(f"Frames processed: "
          f"{end_idx - start_idx}")

    return frame_results


