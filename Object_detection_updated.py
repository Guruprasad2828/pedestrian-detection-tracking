class SORTTracker:
    """
    SORT: Simple Online and Realtime Tracking

    Args:
        max_age     : frames before track deleted
        min_hits    : min hits before track shown
        iou_threshold: min IoU for association
    """

    def __init__(
            self,
            max_age       = 5,
            min_hits      = 3,
            iou_threshold = 0.3):

        self.max_age       = max_age
        self.min_hits      = min_hits
        self.iou_threshold = iou_threshold
        self.trackers      = []
        self.frame_count   = 0

        # Reset track ID counter
        KalmanBoxTracker.count = 0

    def update(self, detections):
        """
        Updates tracker with new detections

        Args:
            detections: (N,5) array
                        [x1,y1,x2,y2,score]

        Returns:
            tracks: (M,5) array
                    [x1,y1,x2,y2,track_id]
        """
        self.frame_count += 1

        # ---- Predict existing trackers ----
        trk_preds = []
        to_delete = []

        for i, trk in enumerate(self.trackers):
            pred = trk.predict()
            if np.any(np.isnan(pred)):
                to_delete.append(i)
            else:
                trk_preds.append(pred)

        # Remove invalid trackers
        for i in reversed(to_delete):
            self.trackers.pop(i)

        trk_arr = np.array(trk_preds) \
            if trk_preds \
            else np.empty((0, 4))

        # ---- Associate detections ----
        det_arr = detections[:, :4] \
            if len(detections) > 0 \
            else np.empty((0, 4))

        matched, unmatched_det, unmatched_trk \
            = associate_detections(
                det_arr, trk_arr,
                self.iou_threshold
            )

        # ---- Update matched trackers ----
        for m in matched:
            self.trackers[m[1]].update(
                detections[m[0], :4]
            )

        # ---- Create new trackers ----
        for d in unmatched_det:
            trk = KalmanBoxTracker(
                detections[d, :4]
            )
            self.trackers.append(trk)

        # ---- Collect active tracks ----
        results  = []
        to_delete = []

        for i, trk in enumerate(self.trackers):
            state = trk.get_state()

            # Only return confirmed tracks
            if (
                trk.time_since_update < 1
                and (
                    trk.hit_streak >= self.min_hits
                    or self.frame_count <= self.min_hits
                )
            ):
                results.append(
                    np.concatenate(
                        [state, [trk.id + 1]]
                    )
                )

            # Mark dead tracks
            if trk.time_since_update > self.max_age:
                to_delete.append(i)

        # Remove dead tracks
        for i in reversed(to_delete):
            self.trackers.pop(i)

        return np.array(results) \
            if results \
            else np.empty((0, 5))


