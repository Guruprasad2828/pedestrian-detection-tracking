def count_parameters(model):
    trainable = sum(
        p.numel()
        for p in model.parameters()
        if p.requires_grad
    )
    frozen = sum(
        p.numel()
        for p in model.parameters()
        if not p.requires_grad
    )
    total = trainable + frozen
    return trainable, frozen, total


