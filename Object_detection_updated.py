def draw_tracks(
        frame, tracks,
        fps=None, n_dets=None,
        show_fps=True,
        show_count=True):
    """Draw bounding boxes + IDs on frame"""

    output = frame.copy()

    # Colors per track ID (BGR)
    colors_bgr = [
        (255,107,107), (78,205,196),
        (69,183,209),  (150,206,180),
        (255,234,167), (221,160,221),
        (152,216,200), (247,220,111),
        (187,143,206), (133,193,233),
        (240,178,122), (130,224,170),
        (241,148,138), (133,146,158),
        (115,198,182), (248,196,113),
    ]

    for track in tracks:
        x1,y1,x2,y2 = [
            int(v) for v in track[:4]
        ]
        tid   = int(track[4])
        color = colors_bgr[
            tid % len(colors_bgr)
        ]

        # Bounding box
        cv2.rectangle(
            output,
            (x1, y1), (x2, y2),
            color, thickness=2
        )

        # Label background
        label      = f'ID:{tid}'
        (tw,th), _ = cv2.getTextSize(
            label,
            cv2.FONT_HERSHEY_SIMPLEX,
            0.6, 2
        )
        cv2.rectangle(
            output,
            (x1, max(y1-th-8, 0)),
            (x1+tw+4, y1),
            color, thickness=-1
        )

        # Label text
        cv2.putText(
            output, label,
            (x1+2, max(y1-4, th)),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.6, (255,255,255),
            thickness=2
        )

    # FPS overlay
    if show_fps and fps is not None:
        cv2.putText(
            output,
            f'FPS: {fps:.1f}',
            (10, 30),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.9, (0,255,0), thickness=2
        )

    # Track count overlay
    if show_count:
        cv2.putText(
            output,
            f'Tracks: {len(tracks)}',
            (10, 65),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.9, (0,255,255), thickness=2
        )
        if n_dets is not None:
            cv2.putText(
                output,
                f'Dets: {n_dets}',
                (10, 100),
                cv2.FONT_HERSHEY_SIMPLEX,
                0.9, (255,165,0), thickness=2
            )

    return output


